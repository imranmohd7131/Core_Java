class relational
{
   public static void main(String[] args)
   {
      int a=10;
      int b=20;
      boolean c=b<a;
      System.out.println(c); 
      boolean d=b>a;
      System.out.println(d);
      boolean e=b<=a;
      System.out.println(e);
      boolean f=b>=a;
      System.out.println(f);
      boolean g=b==a;
      System.out.println(g);
      boolean h=a!=b;
      System.out.println(h);
      
      int aa=25;
      aa+=7;
      int bb=34;
      bb-=2;
      boolean res=aa==bb;
      System.out.println(res);
   }
}